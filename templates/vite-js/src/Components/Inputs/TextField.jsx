import { FormControl, OutlinedInput } from '@mui/material';

const OutlinedTextField = ({name, label, error, helperText, size, type, ...otherProps}) => {
    return (
        <FormControl sx={{ width: "100%" }} size={size} key={name}>
            <OutlinedInput
                id={name}
                fullWidth
                label={label}
                name={name}
                error={error}
                helperText={error && helperText}
                type={type}
                variant="outlined"
                //onChange={}
                {...otherProps}
            />
        </FormControl>
    )
}

export default OutlinedTextField